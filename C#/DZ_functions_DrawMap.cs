        static void SelectDirectionHero(char[,] map, ref int userX, ref int userY, ConsoleKeyInfo tempConsoleKey)
        {
            ConsoleKeyInfo consoleKey = tempConsoleKey;
 
            switch (consoleKey.Key)
            {
                case ConsoleKey.UpArrow:
                    if (map[userX - 1, userY] != '#')
                        userX--;
                    break;
 
